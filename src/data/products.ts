export function getProduct(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductsByCollection(collectionSlug: string): Product[] {
  // Map category-based collection slugs to productCategory values
  const categoryMap: Record<string, string> = {
    socks: "socks",
    apparel: "apparel",
    accessories: "accessories",
  };

  if (collectionSlug === "all") {
    return products;
  }

  const productCat = categoryMap[collectionSlug];
  if (productCat) {
    return products.filter((p) => p.productCategory === productCat);
  }

  return products.filter((p) => p.collections.includes(collectionSlug));
}

export function getRelatedProducts(product: Product, limit = 8): Product[] {
  return products
    .filter(
      (p) =>
        p.id !== product.id &&
        (p.category === product.category ||
          p.collections.some((c) => product.collections.includes(c)))
    )
    .slice(0, limit);
}

